



    var firstNumber = prompt("Enter a First Number : ");
    var secondNumber = prompt("Enter a Second Number : ");

    var cv1 = parseFloat(firstNumber);
    var cv2 = parseFloat(secondNumber);

    var result;

    result = cv1 + cv2;

    document.write(cv1 + " + " + cv2 + " = " + result + "<br>");


    result = cv1 - cv2;

    document.write(cv1 + " - " + cv2 + " = " + result + "<br>");

    result = cv1 * cv2;

    document.write(cv1 + " x " + cv2 + " = " + result + "<br>");

    result = cv1 / cv2;

    document.write(cv1 + " / " + cv2 + " = " + result + "<br>");

    result = cv1 % cv2;

    document.write(cv1 + " % " + cv2 + " = " + result + "<br>");